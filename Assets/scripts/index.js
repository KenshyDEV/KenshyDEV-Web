/*FORMULARIO SCRIPT*/

document.getElementById("myForm").addEventListener("submit", function(event){
    event.preventDefault(); // Evitar que se envíe el formulario de forma predeterminada
    var form = document.getElementById("myForm");
    form.reset(); // Reiniciar el formulario
  });

  $(document).ready(function(){
    $('#myForm').submit(function(event){
      event.preventDefault(); // Evitar que se envíe el formulario de forma predeterminada
      var formData = $(this).serialize(); // Obtener los datos del formulario
      $.ajax({
        type: 'POST',
        url: $(this).attr('action'),
        data: formData,
        dataType: 'json',
        success: function(response){
          alert('¡Gracias por tu mensaje!'); // Mostrar un mensaje de éxito
          $('#myForm')[0].reset(); // Reiniciar el formulario
        },
        error: function(xhr, status, error){
          alert('Ocurrió un error al enviar el formulario. Por favor, inténtalo de nuevo más tarde.'); // Mostrar un mensaje de error
        }
      });
    });
  });





